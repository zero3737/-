package top.zero3737.javabean;

public class CodeLogin {

	private String code;
	private String msg;
	
	public String getCode() {
		return code;
	}
	public CodeLogin setCode(String code) {
		
		this.code = code;
		
		return this;
		
	}
	public String getMsg() {
		return msg;
	}
	public CodeLogin setMsg(String msg) {
		
		this.msg = msg;
		
		return this;
		
	}
	
}
